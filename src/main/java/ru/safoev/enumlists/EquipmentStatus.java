package ru.safoev.enumlists;

/**
 * Перечисление статусов оборудования фитнес-центра.
 * <p>
 * Определяет возможные состояния оборудования в системе.
 * Используется для отслеживания текущего состояния оборудования
 * и управления его доступностью.
 * </p>
 *
 * @author SafoevDalerIT-13
 * @version 1.0
 * @since 2025
 */
public enum EquipmentStatus {

  /**
   * Оборудование активно и доступно для использования.
   * <p>
   * Состояние по умолчанию для нового оборудования.
   * Оборудование в этом статусе может использоваться клиентами.
   * </p>
   */
  ACTIVE,

  /**
   * Оборудование неактивно.
   * <p>
   * Оборудование временно недоступно для использования,
   * но не требует ремонта или не сломано.
   * </p>
   */
  NOT_ACTIVE,

  /**
   * Оборудование находится в ремонте.
   * <p>
   * Оборудование временно недоступно для использования
   * в связи с проведением ремонтных работ.
   * </p>
   */
  REPAIR,

  /**
   * Оборудование сломано.
   * <p>
   * Оборудование вышло из строя и требует ремонта
   * или списания.
   * </p>
   */
  BROKE
}