package ru.safoev.enumlists;

/**
 * Перечисление статусов абонементов фитнес-центра.
 * <p>
 * Определяет возможные состояния абонементов клиентов в системе.
 * Используется для отслеживания текущего состояния абонементов
 * и управления их доступностью.
 * </p>
 *
 * @author SafoevDalerIT-13
 * @version 1.0
 * @since 2025
 */
public enum SubscriptionStatus {

  /**
   * Абонемент активен и действует.
   * <p>
   * Состояние по умолчанию для нового абонемента.
   * Клиент с активным абонементом может посещать фитнес-центр
   * в соответствии с условиями тарифа.
   * </p>
   */
  ACTIVE,

  /**
   * Абонемент неактивен.
   * <p>
   * Абонемент временно или постоянно недействителен.
   * Может быть вызвано истечением срока действия, приостановкой
   * или другими причинами.
   * </p>
   */
  NOT_ACTIVE,

  /**
   * Абонемент заморожен.
   * <p>
   * Действие абонемента временно приостановлено.
   * Срок действия абонемента продлевается на период заморозки.
   * Клиент не может посещать фитнес-центр в этот период.
   * </p>
   */
  FREEZE
}